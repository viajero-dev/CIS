    @Override
    public JasperPrint printNow() throws Exception {
        JasperPrint jasperPrint = null;
        if (optionFront.isSelected()) {
            jasperPrint = ReportController.getInstance().print(formNo, 1, "");
        } else if (optionBack.isSelected()) {
            jasperPrint = ReportController.getInstance().print(formNo, 2, "");
        } else if (optionCM1.isSelected()) {
            jasperPrint = ReportController.getInstance().print(formNo, 3, "CM1");
        } else if (optionCM2.isSelected()) {
            jasperPrint = ReportController.getInstance().print(formNo, 4, "CM2");
        } else if (optionPO.isSelected()) {
            jasperPrint = ReportController.getInstance().printPurchaseOrder(formNo);;
        }

        if (jasperPrint == null) {
            throw new Exception();
        } else {
            return jasperPrint;
        }
    }

