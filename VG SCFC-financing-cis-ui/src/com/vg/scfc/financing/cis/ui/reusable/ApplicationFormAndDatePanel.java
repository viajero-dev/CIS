    @Override
    public void keyReleased(KeyEvent e) {
        switch(e.getKeyCode()) {
            case KeyEvent.VK_ENTER:
                if(txtFormSeries.isFocusOwner()) {
                    txtAppDate.requestFocus();
                }
                break;
            case KeyEvent.VK_UP:
                if(txtAppDate.isFocusOwner()) {
                    txtFormSeries.requestFocus();
                }
                break;
        }
    }

